-module(name_generator).
-export([get_name/0, get_room/0]).

get_room() ->
    random:seed(now()),
    Adjective = get_adjective(random:uniform(1125)),
    random:seed(now()),
    Substantive = get_first_name(random:uniform(1900)),
    Noun = list_to_bitstring(string:to_lower(bitstring_to_list(Substantive))),
    <<Adjective/bitstring, "-", Noun/bitstring>>.

get_name() ->
    random:seed(now()),
    FirstName = get_first_name(random:uniform(1900)),
    random:seed(now()),
    Surename = get_last_name(random:uniform(1000)),
    <<FirstName/bitstring, " ", Surename/bitstring>>.

